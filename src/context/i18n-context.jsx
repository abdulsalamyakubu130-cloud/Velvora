const I18nContext = createContext(null)

function interpolate(template, params) {
  return String(template).replace(/\{(\w+)\}/g, (_match, key) => {
    return params?.[key] ?? ''
  })
}

function sanitizeLanguage(code) {
  if (languageOptions.some((item) => item.code === code)) return code
  return FALLBACK_LANGUAGE
}

export function I18nProvider({ children }) {
  const [language, setLanguage] = useState(() => {
    if (typeof window === 'undefined') return FALLBACK_LANGUAGE
    return sanitizeLanguage(window.localStorage.getItem(STORAGE_KEY) || FALLBACK_LANGUAGE)
  })

  useEffect(() => {
    if (typeof window !== 'undefined') {
      window.localStorage.setItem(STORAGE_KEY, language)
    }

    if (typeof document !== 'undefined') {
      document.documentElement.lang = language
      document.documentElement.dir = language === 'ar' ? 'rtl' : 'ltr'
    }
  }, [language])

  function t(key, params = {}, fallback = '') {
    const template =
      translations[language]?.[key] ??
      translations[FALLBACK_LANGUAGE]?.[key] ??
      fallback ??
      key

    return interpolate(template, params)
  }

  const value = useMemo(
    () => ({
      language,
      setLanguage: (nextLanguage) => setLanguage(sanitizeLanguage(nextLanguage)),
      availableLanguages: languageOptions,
      t,
    }),
    [language],
  )

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const context = useContext(I18nContext)
  if (!context) {
    throw new Error('useI18n must be used within an I18nProvider.')
  }
  return context
}

